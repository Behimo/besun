/** نزدیک‌ترین آموزش برای مسیرهای جزئی (مثلاً جزئیات مخاطب) */
const routeAliases = {
  'apps-crm-contacts-id': 'apps-crm-contacts',
}

export function getTutorialForRoute(routeName) {
  if (!routeName)
    return null

  const key = routeAliases[routeName] ?? routeName

  return pageTutorials[key] ?? null
}

export function isYoutubeUrl(url) {
  if (!url)
    return false

  return /youtu\.be|youtube\.com/.test(url) || /^[a-zA-Z0-9_-]{11}$/.test(url)
}

export function getYoutubeEmbedUrl(url) {
  if (!url)
    return null

  if (/^[a-zA-Z0-9_-]{11}$/.test(url))
    return `https://www.youtube.com/embed/${url}?rel=0&modestbranding=1`

  const match = url.match(/(?:youtu\.be\/|youtube\.com\/(?:embed\/|v\/|watch\?v=))([a-zA-Z0-9_-]{11})/)

  return match ? `https://www.youtube.com/embed/${match[1]}?rel=0&modestbranding=1` : null
}

export function isAparatUrl(url) {
  if (!url)
    return false

  return /aparat\.com/.test(url)
}

export function getAparatEmbedUrl(url) {
  if (!url)
    return null

  const hashMatch = url.match(/aparat\.com\/v\/([a-zA-Z0-9]+)/)
    || url.match(/videohash\/([a-zA-Z0-9]+)/)

  const hash = hashMatch?.[1]

  return hash
    ? `https://www.aparat.com/video/video/embed/videohash/${hash}/vt/frame?titleShow=true&recom=self`
    : null
}

export function getVideoMediaType(url) {
  if (!url)
    return 'poster'

  if (isAparatUrl(url))
    return 'aparat'

  if (isYoutubeUrl(url))
    return 'youtube'

  return 'video'
}
